export interface DemandProduct {
  slug: string;
  name: string;
  category: string;
  categorySlug: string;
  industrySlug: string;
  subIndustrySlug: string;
  /** Short description of what the product is */
  definition: string;
  /** Key industries that procure this */
  industries: string[];
  /** Key grades / variants */
  grades: string[];
  /** Specifications (thickness, sizes, standards) */
  specifications: string[];
  /** Common standards */
  standards: string[];
  /** HSN codes */
  hsnCodes: string[];
  /** Typical order sizes */
  orderSizes: string;
  /** Import source countries */
  importCountries: string[];
  /** Related product slugs */
  relatedSlugs: string[];
  /** Typical price range description */
  priceRange: string;
  /** Key applications */
  applications: string[];
  /** Procurement challenges */
  challenges: string[];
  /** Market trend summary */
  marketTrend: string;
}

function createSlug(product: string): string {
  return product
    .toLowerCase()
    .replace(/[&,()]/g, '')
    .replace(/\s+/g, '-')
    + '-india';
}

